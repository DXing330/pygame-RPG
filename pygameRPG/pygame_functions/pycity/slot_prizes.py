#function that will determine what prize you get
def get_prize(h_bag, bet, line1, line2, line3, line4, line5):
	coins = 0
	width, height = WIN.get_size()
	if line1 == line2 == line3 == line4 == line5:
		if line1 == "777":
			h_bag.coins += bet * 100
			coins = bet * 100
		elif line1 == "LUCK":
			h_bag.coins += bet * 20
			coins = bet * 20
		elif line1 == "WILD":
			h_bag.coins += bet * 10
			coins = bet * 10
		elif line1 == "FREE":
			h_bag.coins += bet * 1
			coins = bet * 1
		elif line1 == "TEN":
			h_bag.coins += bet * 1.5
			coins = bet * 1.5
		elif line1 == "JACK":
			h_bag.coins += bet * 2
			coins = bet * 2
		elif line1 == "QUEEN":
			h_bag.coins += bet * 2.5
			coins = bet * 2.5
		elif line1 == "KING":
			h_bag.coins += bet * 3
			coins = bet * 3
		elif line1 == "ACE":
			h_bag.coins += bet * 5
			coins = bet * 5
		coins = round(coins)
		h_bag.coins = round(h_bag.coins)
		for num in range(0, round(coins**0.5)):
			WIN.fill(P.BLACK)
			line_text = REG_FONT.render(str(line1), 1, P.GREEN)
			WIN.blit(line_text, ((width - line_text.get_width())//2, height//2 - line_text.get_height()))
			win_text = REG_FONT.render("You win "+str(coins)+" coins!", 1, P.GREEN)
			WIN.blit(win_text, ((width - win_text.get_width())//2, height//2 + win_text.get_height()))
			pygame.display.update()
			pygame.time.delay(10)
			WIN.fill(P.BLACK)
			pygame.display.update()
			pygame.time.delay(10)
	elif line1 == "Ten" and line2 == "JacK" and line3 == "Queen" and line4 == "King" and line5 == "Ace":
		h_bag.coins += bet * 50
		coins = bet * 50
		for num in range(0, round(coins**0.5)):
			WIN.fill(P.BLACK)
